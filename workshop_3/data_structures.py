# Data Structure

nums = [1, -2, 4, -7, 987]

print(len(nums))
# access each element by index
for i in range(len(nums)):
    print(f'Number at {i} is {nums[i]}')

for x in nums:
    print(x)

print('\nEnumerate')
for i, num in enumerate(nums):
    print(i, num)

